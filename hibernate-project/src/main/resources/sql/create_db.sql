DROP TABLE HIBERNATE.T_CAR_COLOR;
DROP TABLE HIBERNATE.T_CAR_ENGINE;
DROP TABLE HIBERNATE.T_CAR_TYPE;
DROP TABLE HIBERNATE.T_CAR;
DROP SEQUENCE HIBERNATE.CAR_SEQ;

CREATE TABLE HIBERNATE.T_CAR (
  ID								      NUMBER(19),
  NAME								    VARCHAR2(100 CHAR),
  DATE_ID							    VARCHAR2(100 CHAR),
  CONSTRAINT PK_CAR PRIMARY KEY (ID)
);

CREATE TABLE HIBERNATE.T_CAR_COLOR (
  ID_COLOR								NUMBER(19),
  COLOR								    VARCHAR2(100 CHAR),
  DATE_ID							    VARCHAR2(100 CHAR),
  CONSTRAINT PK_CAR_COLOR PRIMARY KEY (ID_COLOR),
  CONSTRAINT FK_CAR_COLOR FOREIGN KEY (ID_COLOR) REFERENCES T_CAR(ID)
);

CREATE TABLE HIBERNATE.T_CAR_ENGINE (
  ID_ENGINE								NUMBER(19),
  ENGINE							    VARCHAR2(100 CHAR),
  DATE_ID							    VARCHAR2(100 CHAR),
  CONSTRAINT PK_CAR_ENGINE PRIMARY KEY (ID_ENGINE),
  CONSTRAINT FK_CAR_ENGINE FOREIGN KEY (ID_ENGINE) REFERENCES T_CAR(ID)
);

CREATE TABLE HIBERNATE.T_CAR_TYPE (
  ID_TYPE								  NUMBER(19),
  TYPE								    VARCHAR2(100 CHAR),
  DATE_ID							    VARCHAR2(100 CHAR),
  CONSTRAINT PK_CAR_TYPE PRIMARY KEY (ID_TYPE),
  CONSTRAINT FK_CAR_TYPE FOREIGN KEY (ID_TYPE) REFERENCES T_CAR(ID)
);

CREATE SEQUENCE HIBERNATE.CAR_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
